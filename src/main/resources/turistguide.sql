CREATE DATABASE IF NOT EXISTS ATTRACTION_TAGS DEFAULT CHARACTER SET utf8;
USE ATTRACTION_TAGS;
DROP TABLE IF EXISTS ATTRACTION;
DROP TABLE IF EXISTS TAGS;
DROP TABLE IF EXISTS ATTRACTIONTAGS;

CREATE TABLE ATTRACTION (
                            ATTRACTIONID INTEGER AUTO_INCREMENT,
                            ANAME VARCHAR(50),
                            LOC VARCHAR(30),
                            DESCR VARCHAR(255),
                            PRIMARY KEY (ATTRACTIONID)
);

CREATE TABLE TAGS (
                      TAGID INTEGER AUTO_INCREMENT,
                      TAGNAME VARCHAR(15),
                      PRIMARY KEY (TAGID)
);

CREATE TABLE ATTRACTIONTAGS (
                                TAGID INTEGER,
                                ATTRACTIONID INTEGER,
                                PRIMARY KEY(TAGID, ATTRACTIONID),
                                FOREIGN KEY (ATTRACTIONID) REFERENCES ATTRACTION(ATTRACTIONID),
                                FOREIGN KEY (TAGID) REFERENCES TAGS(TAGID)
);

